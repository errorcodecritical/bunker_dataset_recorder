// Program to register callbacks and subscribe to the compensated point cloud, un-compensated point
// cloud, and point group point cloud datastreams.

#include <signal.h>
#include <unistd.h>
#include <chrono>
#include <iostream>
#include <string>

#include "aeva/api/AevaAPI.h"

using namespace aeva;

// Time interval to poll the Aeva API.
static constexpr int64_t kPollIntervalMilliseconds = 5;

int main(int argc, char** argv) {
  if (argc < 3) {
    std::cerr << "Not enough arguments. Please provide the sensor IP and run duration (secs)."
              << std::endl;
    return EXIT_FAILURE;
  }

  const auto runtime_duration_secs = std::chrono::seconds(std::stoi(argv[2]));

  // Assign sensor info.
  api::AevaAPI api;
  api::Sensor sensor;
  sensor.id_ = "sensor";
  sensor.url_ = argv[1];
  sensor.protocol_ = api::Sensor::Protocol::TCP;
  sensor.platform_.type_ = api::Sensor::Platform::Type::ATLAS;

  // Connect to the sensor.
  if (!api.Connect(sensor)) {
    std::cerr << "Failed to connect to the sensor at " << argv[1] << std::endl;
    return EXIT_FAILURE;
  }

  std::size_t compensated_point_clouds{0};
  std::size_t frame_point_clouds{0};
  std::size_t point_group_point_clouds{0};

  // Register callbacks.
  api.RegisterPointCloudMessageCallback(
      api::kPointCloudDataStreamId, [&](const api::PointCloud& message) {
        compensated_point_clouds++;
        std::cout << "Compensated: " << message.header_.acquisition_time_.ToNanoSeconds()
                  << " | ID: " << message.header_.sequenceID_ << std::endl;
      });

  api.RegisterPointCloudMessageCallback(
      api::kPointCloudFrameDataStreamId, [&](const api::PointCloud& message) {
        frame_point_clouds++;
        std::cout << "Frame: " << message.header_.acquisition_time_.ToNanoSeconds()
                  << " | ID: " << message.header_.sequenceID_ << std::endl;
      });

  api.RegisterPointCloudMessageCallback(
      api::kPointCloudPointGroupDataStreamId, [&](const api::PointCloud& message) {
        point_group_point_clouds++;
        std::cout << "Point Group: " << message.header_.acquisition_time_.ToNanoSeconds()
                  << " | ID: " << message.header_.sequenceID_ << std::endl;
      });

  // Full frame point cloud stream that has range and velocity compensation applied.
  api.Subscribe(sensor.id_, api::kPointCloudDataStreamId);

  // Full frame point cloud stream that has neither range not velocity compensation applied.
  api.Subscribe(sensor.id_, api::kPointCloudFrameDataStreamId);

  // These are low-latency point groups generated by the Aeva API in Cartesian format at 120 Hz.
  // This stream is different from the raw SPC packets received directly from the sensor.
  // This point cloud stream also has neither range not velocity compensation applied.
  api.Subscribe(sensor.id_, api::kPointCloudPointGroupDataStreamId);

  // Execute the callback after every 5 ms using PollEvents.
  const auto start_time = std::chrono::high_resolution_clock::now();
  const auto stop_time = start_time + runtime_duration_secs;
  while (std::chrono::high_resolution_clock::now() < stop_time) {
    api.PollEvents();
    usleep(kPollIntervalMilliseconds * 1000);
  }

  // Disconnect from the sensor unit.
  api.Disconnect(sensor.id_);

  std::cout << "\n# Compensated Point Clouds: " << compensated_point_clouds
            << "\n# Frame Point Clouds: " << frame_point_clouds
            << "\n# Point Group Point Clouds: " << point_group_point_clouds << std::endl;

  return EXIT_SUCCESS;
}
