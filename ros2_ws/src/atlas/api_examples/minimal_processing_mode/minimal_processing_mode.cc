// Program to register callbacks and subscribe to the point group point cloud datastream using the
// minimal processing in the Aeva API.

#include <signal.h>
#include <unistd.h>
#include <chrono>
#include <iostream>
#include <string>

#include "aeva/api/AevaAPI.h"

using namespace aeva;

// Time interval to poll the Aeva API.
static constexpr int64_t kPollIntervalMilliseconds{5};

// Flag to indicate if we are still running.
volatile bool Running = true;

constexpr std::size_t kMaxTries{5};

std::string CodeToString(api::ConfigReturn code) {
  if (code == api::ConfigReturn::OK) {
    return "OK";
  } else if (code == api::ConfigReturn::FAIL) {
    return "FAIL";
  } else if (code == api::ConfigReturn::TIMEOUT) {
    return "TIMEOUT";
  } else if (code == api::ConfigReturn::INVALID_SENSOR) {
    return "INVALID_SENSOR";
  } else {
    return "UNKNOWN";
  }
}

template <typename ConfigValueT>
bool SetConfig(api::AevaAPI& api, const std::string& sensor_id, const std::string& node,
               const std::string& key, const ConfigValueT& value) {
  auto code = api.ConfigureValue(sensor_id, node, key, value, api::Time::FromSeconds(3));
  if (code != api::ConfigReturn::OK) {
    std::cerr << "Failed to set config: " << node << "." << key << " = " << value
              << " [status: " << CodeToString(code) << "]" << std::endl;
    return false;
  }
  return true;
}

void SignalHandler(int signum) {
  if (signum == SIGINT) {
    std::cout << "Received SIGINT" << std::endl;
    Running = false;
  }
}

int main(int argc, char** argv) {
  if (argc < 2) {
    std::cerr << "Not enough arguments. Please provide the sensor IP." << std::endl;
    return EXIT_FAILURE;
  }

  signal(SIGINT, SignalHandler);

  // Assign sensor info.
  api::AevaAPI api;
  api::Sensor sensor;
  sensor.id_ = "sensor";
  sensor.url_ = argv[1];
  sensor.protocol_ = api::Sensor::Protocol::TCP;
  sensor.platform_.type_ = api::Sensor::Platform::Type::ATLAS;

  // Connect to the sensor in minimal processing mode.
  // This processing mode by default only outputs the Cartesian point groups from the sensor.
  if (!api.Connect(sensor, api::ProcessingMode::MINIMAL)) {
    std::cerr << "Failed to connect to the sensor at " << argv[1] << std::endl;
    return EXIT_FAILURE;
  }

  // Optionally toggle full frame point cloud outputs.
  // Full frame outputs are bypassed by default.
  if (!SetConfig<bool>(api, sensor.id_, "host_frame_builder", "bypass", true)) {
    return EXIT_FAILURE;
  }

  std::size_t frame_point_clouds{0};
  std::size_t point_group_point_clouds{0};

  // Register callbacks.

  api.RegisterPointCloudMessageCallback(
      api::kPointCloudFrameDataStreamId, [&](const api::PointCloud& message) {
        frame_point_clouds++;
        std::cout << "Full Frame: " << message.header_.acquisition_time_.ToNanoSeconds()
                  << " | ID: " << message.header_.sequenceID_ << std::endl;
      });

  api.RegisterPointCloudMessageCallback(
      api::kPointCloudPointGroupDataStreamId, [&](const api::PointCloud& message) {
        point_group_point_clouds++;
        std::cout << "Point Group: " << message.header_.acquisition_time_.ToNanoSeconds()
                  << " | ID: " << message.header_.sequenceID_ << std::endl;
      });

  // Full frame point cloud stream that has neither range not velocity compensation applied.
  api.Subscribe(sensor.id_, api::kPointCloudFrameDataStreamId);

  // These are low-latency point groups generated by the Aeva API in Cartesian format at 120 Hz.
  // This point cloud stream also has neither range not velocity compensation applied.
  api.Subscribe(sensor.id_, api::kPointCloudPointGroupDataStreamId);

  // Execute the callback after every 5 ms using PollEvents.
  while (Running) {
    api.PollEvents();
    usleep(kPollIntervalMilliseconds * 1000);
  }

  // Disconnect from the sensor unit.
  api.Disconnect(sensor.id_);

  std::cout << "\n# Frame Point Clouds: " << frame_point_clouds
            << "\n# Point Groups: " << point_group_point_clouds << std::endl;

  return EXIT_SUCCESS;
}
